package com.zr.model;

public class Exam {
	private int e_id;
	private String e_name;
	private String e_starttime;
	private String e_endtime;
	private String e_state; 
	private int e_total;
	public int getE_id() {
		return e_id;
	}
	public void setE_id(int e_id) {
		this.e_id = e_id;
	}
	public String getE_name() {
		return e_name;
	}
	public void setE_name(String e_name) {
		this.e_name = e_name;
	}
	public String getE_starttime() {
		return e_starttime;
	}
	public void setE_starttime(String e_starttime) {
		this.e_starttime = e_starttime;
	}
	public String getE_endtime() {
		return e_endtime;
	}
	public void setE_endtime(String e_endtime) {
		this.e_endtime = e_endtime;
	}
	public String getE_state() {
		return e_state;
	}
	public void setE_state(String e_state) {
		this.e_state = e_state;
	}
	public int getE_total() {
		return e_total;
	}
	public void setE_total(int e_total) {
		this.e_total = e_total;
	}
	
}
